//Crie um loop que exiba todos os números pares de 0 a 20.
let i = 0; // Inicializa o contador
// Loop while para exibir números pares de 0 a 20
while (i <= 20) {
    console.log(`Numeros pares entre zero e vinte : ${i}`); // Imprime o número par
    i += 2; // Incrementa o contador em 2 para obter o próximo número par
}
