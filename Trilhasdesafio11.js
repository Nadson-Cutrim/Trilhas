//Crie um loop que imprima a tabuada do número 7, de 1 a 10.
const numero = 7; // constante: Número para o qual queremos a tabuada
let i = 1; // Inicializa o contador

// Loop while para imprimir a tabuada do número 7
while (i <= 10) {
    console.log(`Resultado da Multipicação: ${numero} x ${i} = ${numero * i}`); // Imprime a multiplicação
    i++; // Incrementa o contador
}
