//Escreva um código que calcule a área de um círculo. Utilize uma função para realizar o cálculo. A função deve receber o raio como parâmetro e retornar a área.
let raio = 10; // Raio do círculo
let area = Math.PI * raio * raio; // Expressão de Calculo da área do círculo
console.log(`A área do círculo é ${area.toFixed()}`); // resultado da área do círculo 
